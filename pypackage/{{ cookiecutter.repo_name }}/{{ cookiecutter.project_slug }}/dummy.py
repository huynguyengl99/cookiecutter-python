"""
This module provides a simple function for incrementing integer values.

Example:
    >>> from dummy import dump_func
    >>> dump_func(5)
    7
"""


def dump_func(a: int) -> int:
    """
    Increments the given integer value by 2.

    Args:
        a (int): The input integer value.

    Returns:
        int: The input value incremented by 2.
    """
    return a + 2
